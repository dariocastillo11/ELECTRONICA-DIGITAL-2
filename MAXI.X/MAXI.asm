LIST P=16F887
 #INCLUDE "P16F887.inc"
    __CONFIG _CONFIG1, _FOSC_INTRC_NOCLKOUT & _WDTE_OFF & _MCLRE_ON & _LVP_OFF
    ORG 0X00
    GOTO 0X05
    ORG 0X05
    
CONTADOR1 EQU 0X30
CONTADOR2 EQU 0X31
CONTADOR3 EQU 0X32
INICIO EQU 0X70

SETUP:
    CLRF CONTADOR3
    
    MOVLW 0X3F;0
    MOVWF 0X70
    
    MOVLW 0X06;1
    MOVWF 0X71
    
    MOVLW 0X5B;2
    MOVWF 0X72
    
    MOVLW 0X4F;3
    MOVWF 0X73
    
    MOVLW 0X66;4
    MOVWF 0X74
    
    MOVLW 0X6D;5
    MOVWF 0X75
    
    MOVLW 0X7D;6
    MOVWF 0X76
    
    MOVLW 0X07;7
    MOVWF 0X77
    
    MOVLW 0X7F;8
    MOVWF 0X78
    
    MOVLW 0X67;9
    MOVWF 0x79
    
    MOVLW 0X70
    MOVWF FSR
    
    BANKSEL ANSELH
    CLRF ANSELH
    CLRF ANSEL
    BANKSEL TRISA
    CLRF TRISA
    BANKSEL PORTA
    CLRF PORTA
    MOVLW .10
    MOVWF CONTADOR3
    
LOOP:
    MOVF INDF, W
    CLRF PORTA
    MOVWF PORTA
    CALL DELAY1
    INCF FSR, F
    DECFSZ CONTADOR3, F
    GOTO LOOP
    MOVLW 0X70
    MOVWF FSR
    MOVLW .10
    MOVWF CONTADOR3
    GOTO LOOP
    
DELAY1:
    MOVLW .250
    MOVWF CONTADOR1
L1:
    NOP
    NOP
    MOVLW .250
    MOVWF CONTADOR2
L2:
    NOP
    NOP
    DECFSZ CONTADOR2, F
    GOTO L2
    DECFSZ CONTADOR1, F
    GOTO L1
    RETURN
   
    END